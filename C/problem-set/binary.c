void search(int array[], int n, int target)
{
    int start = 0;
    int end = n - 1;
    int middle = (start + end) / 2;

    while(start < end)
    {
        if(target == array[middle])
        {
            printf("target found, position: %i\n", middle);
            return;
        }
        else if (target < array[middle])
        {
            end = middle - 1;
        }
        else if (target > array[middle])
        {
            start = middle + 1;
        }
        middle = (start + end) / 2;
    }
    printf("ERROR 1\ntarget not found :(\n");
}
